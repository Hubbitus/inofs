////////////////////////////////////////////////////////////////////////////////

#include "Singleton.hpp"

////////////////////////////////////////////////////////////////////////////////
Singleton::Singleton()
	: pLog_(new log()){
}

////////////////////////////////////////////////////////////////////////////////
Singleton::~Singleton(){
	if ( m_Instance != 0 ){
	delete m_Instance;
	m_Instance = 0;
	}
// Other, instead of plain pointers we use smart anought boost::shared_ptr<>
//	to do not cleanup it directly
}

////////////////////////////////////////////////////////////////////////////////
Singleton*
Singleton::GetInstance(){
return m_Instance;
}

////////////////////////////////////////////////////////////////////////////////
boost::shared_ptr<log>
Singleton::GetLog(){
return pLog_;
}

////////////////////////////////////////////////////////////////////////////////
Singleton* Singleton::m_Instance = new Singleton();
